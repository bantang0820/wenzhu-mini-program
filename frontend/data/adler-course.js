function buildGeneratedLessons(chapterConfigs = [], templates = [], chapterMetaMap = {}) {
  return chapterConfigs.reduce((acc, chapter) => {
    const chapterLessonMeta = chapterMetaMap[chapter.id] || [];
    acc[chapter.id] = templates.map((template, index) => {
      const meta = chapterLessonMeta[index] || {};
      return {
      id: index + 1,
      title: meta.title || template.title,
      theory: meta.theory || template.theory,
      breakthrough: meta.breakthrough || template.breakthrough,
      insight: template.buildInsight(chapter),
      action: template.buildAction(chapter),
      completed: false
      };
    });
    return acc;
  }, {});
}

