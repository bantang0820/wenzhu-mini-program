const growthLessonsByChapter = buildGeneratedLessons(growthChapterConfigs, growthLessonTemplates, growthLessonMetaByChapter);

const customCourseMap = {
  adler: {
    chapterMetas: adlerChapterMetas,
    lessonsByChapter: adlerLessonsByChapter
  },
  self: {
    chapterMetas: selfChapterMetas,
    lessonsByChapter: selfLessonsByChapter
  },
  growth: {
    chapterMetas: growthChapterMetas,
    lessonsByChapter: growthLessonsByChapter
  }
};

function getSafeChapterId(chapterId) {
  return Math.max(1, Math.min(10, Number(chapterId || 1)));
}

function cloneLessons(lessons = []) {
  return lessons.map((lesson) => ({ ...lesson }));
}

function hasCustomCourse(albumId = '') {
  return !!customCourseMap[albumId];
}

function getCustomCourseChapterMeta(albumId, chapterId) {
  if (!hasCustomCourse(albumId)) return null;
  const safeId = getSafeChapterId(chapterId);
  return customCourseMap[albumId].chapterMetas[safeId - 1] || null;
}

function getCustomCourseLessons(albumId, chapterId) {
  if (!hasCustomCourse(albumId)) return [];
  const safeId = getSafeChapterId(chapterId);
  return cloneLessons(customCourseMap[albumId].lessonsByChapter[safeId] || []);
}

function getAdlerChapterMeta(chapterId) {
  return getCustomCourseChapterMeta('adler', chapterId);
}

function getAdlerLessons(chapterId) {
  return getCustomCourseLessons('adler', chapterId);
}

function getSelfChapterMeta(chapterId) {
  return getCustomCourseChapterMeta('self', chapterId);
}

function getSelfLessons(chapterId) {
  return getCustomCourseLessons('self', chapterId);
}

function getGrowthChapterMeta(chapterId) {
  return getCustomCourseChapterMeta('growth', chapterId);
}

function getGrowthLessons(chapterId) {
  return getCustomCourseLessons('growth', chapterId);
}

